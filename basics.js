// Variables
var name = "Subasri";   // can be redeclared
let age = 22;           // block scoped
const country = "India"; // cannot be changed

console.log(name, age, country);

// Constant
let str = "Hello";      // string
let num = 25;           // number
let isStudent = true;   // boolean
let notDefined;         // undefined
let empty = null;       // null
let arr = [1, 2, 3];    // array
let obj = { name: "Subasri", age: 22 }; // object
console.log(str,num,isStudent,notDefined,empty,arr,obj)

// Basic operators
let x = 10, y = 5;

// Arithmetic
console.log(x + y); // 15
console.log(x * y); // 50

// Comparison
console.log(x > y);  // true
console.log(x === 10); // true

// Logical
console.log(x > 5 && y < 10); // true
